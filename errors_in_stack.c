#include "push_swap.h"

int	check_int(t_lst **a)
{
	t_lst	*rep_a;

	rep_a = (*a);
	while (rep_a)
	{
		if (rep_a->nbr > 2147483647 || rep_a->nbr < -2147483648)
			return (0);
		rep_a = rep_a->next;
	}
	return (1);
}
